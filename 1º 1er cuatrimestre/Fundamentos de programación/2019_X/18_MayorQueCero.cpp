#include <iostream>

using namespace std;


int main(){


	double variableX;

	cout << "Introducir valor para X : ";
	cin >> variableX;

	if(variableX > 0)
		cout << "Es mayor que 0" << endl;
	else
		cout << "No es mayor que 0" << endl;


	return 0;
}

